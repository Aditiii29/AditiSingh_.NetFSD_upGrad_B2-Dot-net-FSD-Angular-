// Store signal color
let signal = "yellow";   // Change to "red", "green", or any value

// Use switch statement
switch (signal) {

    case "red":
        console.log("Stop");
        break;

    case "yellow":
        console.log("Get Ready");
        break;

    case "green":
        console.log("Go");
        break;

    default:
        console.log("Invalid signal color");
}
