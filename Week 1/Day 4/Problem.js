// Accept student's marks
let marks = 72;   // You can change this value

// Grade evaluation using if-else
if (marks >= 75) {
    console.log("Grade A");
} 
else if (marks >= 60) {
    console.log("Grade B");
} 
else if (marks >= 40) {
    console.log("Grade C");
} 
else {
    console.log("Fail");
}
